package proj1;
import java.util.*;


public class Hospede {

	int id;
	int canal;
	long tTv;
	long tDormir;
	
	// Get Set
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getCanal() {
		return canal;
	}
	public void setCanal(int canal) {
		this.canal = canal;
	}
	public long gettTv() {
		return tTv;
	}
	public void settTv(long tTv) {
		this.tTv = tTv;
	}
	public long gettDormir() {
		return tDormir;
	}
	public void settDormir(long tDormir) {
		this.tDormir = tDormir;
	}
	
}
